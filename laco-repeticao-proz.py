# com while crescente
andar = 0
while andar <= 20:
    if andar != 13:
        print(andar)
    andar += 1
# com for crescente
for andar1 in range(0, 20, 1):
    if andar1 != 13:
        print(andar1)
# com while decrescente
andar = 20
while andar > 0:
    if andar != 13:
        print(andar)
    andar -= 1

